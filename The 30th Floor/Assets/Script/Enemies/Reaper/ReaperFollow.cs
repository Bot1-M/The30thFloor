using UnityEngine;

public class ReaperFollow : StateMachineBehaviour
{

    [SerializeField] private float speed;
    [SerializeField] private float baseTime;
    private float timeToFollow;
    private Transform player;
    private ReaperEnemy reaperEnemy;


    // OnStateEnter is called when a transition starts and the state machine starts to evaluate this state
    override public void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        player = GameObject.FindGameObjectWithTag("Player").transform;
        reaperEnemy = animator.GetComponent<ReaperEnemy>();
        timeToFollow = baseTime;
    }

    // OnStateUpdate is called on each Update frame between OnStateEnter and OnStateExit callbacks
    override public void OnStateUpdate(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        animator.transform.position = Vector2.MoveTowards(animator.transform.position, player.position, speed * Time.deltaTime);
        reaperEnemy.Spin(player.position);
        timeToFollow -= Time.deltaTime;
        if (timeToFollow <= 0)
        {
            animator.SetTrigger("Comeback");
        }
    }

